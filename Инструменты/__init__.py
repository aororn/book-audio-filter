# Яндекс Спич - Инструменты для проверки транскрипции
#
# Ядро:
#   config              - Централизованная конфигурация (пути, настройки, логирование)
#   pipeline            - Главный пайплайн проверки
#   batch_pipeline      - Пакетная обработка нескольких глав
#
# Обработка:
#   transcribe          - Транскрибация через Яндекс SpeechKit
#   audio_converter     - Конвертация аудио в OggOpus
#   text_normalizer     - Нормализация текста (числа, сокращения, дефисы)
#   smart_compare       - Умное сравнение транскрипции с оригиналом
#   golden_filter       - Многоуровневый фильтр ложных срабатываний (20 уровней)
#
# Вспомогательные:
#   morphology          - Морфологический анализ (pymorphy2 + кэш)
#   cache_manager       - Кэширование результатов
#   proper_names_extractor - Извлечение имён собственных из текста
#   transcription_provider - Абстракция интерфейса транскрибации (ABC)
#
# Пакеты:
#   filters/            - Модульный пакет фильтрации (constants, comparison, detectors, engine)
#
# Интерфейсы:
#   web_viewer          - Веб-интерфейс с аудиоплеером

